    private void excluir() {

        java.sql.Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;

        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Excluir este Usu√°rio?", "Aten√ß√£o!", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {

            String sql = "DELETE FROM usuarios where idusuarios =?";
            try {
                stmt = con.prepareStatement(sql);
                stmt.setString(1, id);
                int apagado = stmt.executeUpdate();
                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "Usu√°rio exclu√≠do com sucesso!");
                    listarUsuarios();
                    limparFormulario();

                }

            } catch (java.sql.SQLIntegrityConstraintViolationException e) {
                JOptionPane.showMessageDialog(null, "Usu√°rio n√£o pode ser exclu√≠do!" , "Aten√ßao!" ,JOptionPane.ERROR_MESSAGE);
            } catch (Exception ee) {
                JOptionPane.showMessageDialog(null, ee);
                System.out.println(ee);
            } finally {
            ConnectionFactory.closeConnection(con, stmt);
        }

        }

    }
