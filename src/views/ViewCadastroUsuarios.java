    // In√≠cio M√©todo para salvar um Usu√°rio
    private void salvarUsuario() {

        java.sql.Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;

        String sql = "INSERT  INTO usuarios "
                + "( nome, "
                + "login,   "
                + "senha, "
                + "perfil ) "
                + " VALUES (?,?,?,?)";
        try {
            stmt = con.prepareStatement(sql);

            stmt.setString(1, tNome.getText());
            stmt.setString(2, tLogin.getText());
            stmt.setString(3, tSenha.getText());
            stmt.setString(4, cbPerfil.getSelectedItem().toString());

            // A linha abaixo atualiza os dados     
            int adicionado = stmt.executeUpdate();

            if (adicionado > 0) {
                JOptionPane.showMessageDialog(null, "Usu√°rio cadastrado com sucesso!");
                listarUsuarios();
                limparFormulario();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Falha ao cadastrar Usu√°rio " + "\n" + e , "Aten√ßao!" ,JOptionPane.ERROR_MESSAGE );
        } finally {
            ConnectionFactory.closeConnection(con, stmt);
        }
    }
    // Fim M√©todo para salvar um Usu√°rio

